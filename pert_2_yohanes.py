# =========================
# No 3 - Additional Convention
# =========================

octal_val = 0o12
hex_val = 0x1A

print(f"Nilai Octal (0o12) sebagai Desimal : {octal_val}")
print(f"Nilai Hexadecimal (0x1A) sebagai Desimal : {hex_val}")
print("Format String Octal")
print("Format String Hex")

print("\n")


